let num = parseFloat(prompt("Enter the marks: "));


if(num>=80 && num<=100){console.log("A+");}
else if(num<80 && num>=70){console.log("A");}
else if(num<70 && num>=60){console.log("A-");}
else if(num<70 && num>=60){console.log("B");}
else if(num>=0 && num<60){console.log("Fail");}
else{console.log("Wrong number");}
